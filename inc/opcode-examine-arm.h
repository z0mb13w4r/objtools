#ifndef __OPCODE_EXAMINE_ARM_H_
#define __OPCODE_EXAMINE_ARM_H_

#define OCINSNARM_BR               ((0x04) | OCINSN_OPERAND1)

#define OCINSNARM_PUSH             ((0x0b) | OCINSN_OPERAND1)
#define OCINSNARM_POP              ((0x0c) | OCINSN_OPERAND1)
#define OCINSNARM_ADD              ((0x0d) | OCINSN_OPERAND2)
#define OCINSNARM_SUB              ((0x18) | OCINSN_OPERAND2)
#define OCINSNARM_LDR              ((0x20) | OCINSN_OPERAND2)
#define OCINSNARM_STR              ((0x21) | OCINSN_OPERAND2)

#define OCINSNARM_CLR              U64MASK(16)
#define OCINSNARM_SET              U64MASK(17)
#define OCINSNARM_LINK             U64MASK(18)

#define OCINSNARM_CC               (OCINSN_C | OCINSNARM_CLR)
#define OCINSNARM_CS               (OCINSN_C | OCINSNARM_SET)
#define OCINSNARM_EQ               (OCINSN_E)
#define OCINSNARM_MI               (OCINSN_N | OCINSNARM_SET)
#define OCINSNARM_NE               (OCINSN_NE)
#define OCINSNARM_PL               (OCINSN_N | OCINSNARM_CLR)
#define OCINSNARM_VC               (OCINSN_O | OCINSNARM_CLR)
#define OCINSNARM_VS               (OCINSN_O | OCINSNARM_SET)

#define OCINSNARM_BCC              (OCINSNARM_BR | OCINSNARM_CC)
#define OCINSNARM_BCS              (OCINSNARM_BR | OCINSNARM_CS)
#define OCINSNARM_BEQ              (OCINSNARM_BR | OCINSNARM_EQ)
#define OCINSNARM_BGE              (OCINSNARM_BR | OCINSN_GE)
#define OCINSNARM_BGT              (OCINSNARM_BR | OCINSN_G)
#define OCINSNARM_BHI              (OCINSNARM_BR | OCINSN_Z | OCINSNARM_CLR)
#define OCINSNARM_BLE              (OCINSNARM_BR | OCINSN_LE)
#define OCINSNARM_BLS              (OCINSNARM_BR | OCINSN_Z | OCINSNARM_SET)
#define OCINSNARM_BLT              (OCINSNARM_BR | OCINSN_L)
#define OCINSNARM_BMI              (OCINSNARM_BR | OCINSNARM_MI)
#define OCINSNARM_BNE              (OCINSNARM_BR | OCINSNARM_NE)
#define OCINSNARM_BPL              (OCINSNARM_BR | OCINSNARM_PL)
#define OCINSNARM_BVC              (OCINSNARM_BR | OCINSNARM_VC)
#define OCINSNARM_BVS              (OCINSNARM_BR | OCINSNARM_VS)

#define OCINSNARM_BL               (OCINSNARM_BR | OCINSNARM_LINK)
#define OCINSNARM_BLCC             (OCINSNARM_BCC | OCINSNARM_LINK)
#define OCINSNARM_BLCS             (OCINSNARM_BCS | OCINSNARM_LINK)
#define OCINSNARM_BLEQ             (OCINSNARM_BEQ | OCINSNARM_LINK)
#define OCINSNARM_BLGE             (OCINSNARM_BGE | OCINSNARM_LINK)
#define OCINSNARM_BLGT             (OCINSNARM_BGT | OCINSNARM_LINK)
#define OCINSNARM_BLHI             (OCINSNARM_BHI | OCINSNARM_LINK)
#define OCINSNARM_BLLE             (OCINSNARM_BLE | OCINSNARM_LINK)
#define OCINSNARM_BLLS             (OCINSNARM_BLS | OCINSNARM_LINK)
#define OCINSNARM_BLLT             (OCINSNARM_BLT | OCINSNARM_LINK)
#define OCINSNARM_BLMI             (OCINSNARM_BMI | OCINSNARM_LINK)
#define OCINSNARM_BLNE             (OCINSNARM_BNE | OCINSNARM_LINK)
#define OCINSNARM_BLPL             (OCINSNARM_BPL | OCINSNARM_LINK)
#define OCINSNARM_BLVC             (OCINSNARM_BVC | OCINSNARM_LINK)
#define OCINSNARM_BLVS             (OCINSNARM_BVS | OCINSNARM_LINK)

#define OCINSNARM_LDRCC            (OCINSNARM_LDR | OCINSNARM_CC)
#define OCINSNARM_LDRCS            (OCINSNARM_LDR | OCINSNARM_CS)
#define OCINSNARM_LDREQ            (OCINSNARM_LDR | OCINSNARM_EQ)
#define OCINSNARM_LDRMI            (OCINSNARM_LDR | OCINSNARM_MI)
#define OCINSNARM_LDRNE            (OCINSNARM_LDR | OCINSNARM_NE)
#define OCINSNARM_LDRPL            (OCINSNARM_LDR | OCINSNARM_PL)
#define OCINSNARM_LDRVC            (OCINSNARM_LDR | OCINSNARM_VC)
#define OCINSNARM_LDRVS            (OCINSNARM_LDR | OCINSNARM_VS)

#define OCINSNARM_STRCC            (OCINSNARM_STR | OCINSNARM_CC)
#define OCINSNARM_STRCS            (OCINSNARM_STR | OCINSNARM_CS)
#define OCINSNARM_STREQ            (OCINSNARM_STR | OCINSNARM_EQ)
#define OCINSNARM_STRMI            (OCINSNARM_STR | OCINSNARM_MI)
#define OCINSNARM_STRNE            (OCINSNARM_STR | OCINSNARM_NE)
#define OCINSNARM_STRPL            (OCINSNARM_STR | OCINSNARM_PL)
#define OCINSNARM_STRVC            (OCINSNARM_STR | OCINSNARM_VC)
#define OCINSNARM_STRVS            (OCINSNARM_STR | OCINSNARM_VS)

#endif

